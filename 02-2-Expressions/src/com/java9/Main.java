package com.java9;

public class Main {

    public static void main(String[] args) {
        System.out.print(3 * (5 + 2));
        System.out.print('\n');

        System.out.print(65 / 3.141592653);
        System.out.print('\n');

        System.out.print(475 % 8); // Modulo, i.e. n is now (m mod 8)
        System.out.print('\n');

        System.out.print(true);
        System.out.print('\n');

        System.out.print(!true);
        System.out.print('\n');

        System.out.print('x');
        System.out.print('\n');
    }
}
